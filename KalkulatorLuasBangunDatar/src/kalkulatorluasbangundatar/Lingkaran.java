    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(input.getText().equals("")) {
            output.setText("Kotak tidak boleh kosong!");
            output.setVisible(true);
        } else {
            double sisi = Double.parseDouble(input.getText());
            double hasil = Math.PI*sisi*sisi;
            if (sisi % 7 == 0) {
                hasil = Math.round(hasil);
            } else {
                hasil = Math.round(hasil*100.00)/100.00;
            }
            output.setText("Luas = "+hasil+" cm2");
            output.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

